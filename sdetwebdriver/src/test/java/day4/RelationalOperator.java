package day4;

public class RelationalOperator {

	public static void main(String[] args) {
		int a=18, b= 4;
		
		System.out.println(a>b); //it gives the boolean value //true
		System.out.println(a>=b);//true
		System.out.println(a<b); //false
		System.out.println(a<=b); // false
		System.out.println(a!=b);//true
		System.out.println(a==b); //false

	}

}
