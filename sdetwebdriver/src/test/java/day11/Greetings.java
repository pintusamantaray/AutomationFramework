package day11;

public class Greetings
{
	//No Params //No Return Value
	void m1() 
	{
		System.out.println("Hello...");
	}
	//No Params //Return Value
	String m2() 
	{
		return "How are you";
		
	}
	//Take Params //No Return Value
	void m3(String name)
	{
		System.out.println("Hello "+name);
	}
	//Take Params //Return Value
	int m4(int age)
	{
		return age;
		
	}
	//Example using Constructor

}
